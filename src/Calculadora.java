public class Calculadora
{
    public Integer sumarElementos (int elemento1, int elemento2)
    {
        return elemento1+elemento2;
    }

    public Integer restarElementos (int elemento1, int elemento2)
    {
        return elemento1-elemento2;
    }

    public Integer multiplicarElementos (int elemento1, int elemento2)
    {
        return elemento1*elemento2;
    }

    public Integer dividirElementos (int elemento1, int elemento2)
    {
        return elemento1/elemento2;
    }
}